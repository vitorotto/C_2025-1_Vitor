#include<stdio.h>

int main(){
  //Impressão de varíavel inteira
  int numero = 10;
  printf("O valor de numero e %d\n",numero);

  //Impressão de variável real (float  ou double)
  float altura = 1.8;
  double valor = 5.965965;
  printf("A variavel altura vale %f e a variavel valor vale %.3f\n",altura, valor);
  //Impressão de um tipo char
  char letra = 'b';
  printf("A letra e %c\n",letra);

  

  return 0;
}